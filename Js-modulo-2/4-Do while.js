// while & do while
//enquanto for verdadeiro, ele não para

let i = 0

//se for verdadeiro vai excutar até se tornar falso
while (i < 4) {
    i++
    console.log('É TRUE')
}

let x = 0
//primeiro ele faz e depois verifica se é verdadeiro
do {
    x++
    console.log('É verdadeiro')
} while (x < 10);